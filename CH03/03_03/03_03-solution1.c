#include <stdio.h>

int main()
{
	/* variable declarations */
	int a,c; /*integer variables*/
	float b; /*floating point variable*/

	/* variable assignments */
	a = 15; /*integer variable*/
	b = a/2.0; /*floating point variable*/
	c = b * 4; /*integer variable*/

	/* output */
	printf("Value of variable A = %d\n",a); /*integer variable*/
	printf("Value of variable B = %f\n",b); /*floating point variable*/
	printf("Value of variable C = %d\n",c); /*integer variable*/

	return(0);
}
